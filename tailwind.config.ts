type Colors = Record<string, string | Record<string, string>>;
type FlattenColorPaletteType = (colors: Colors) => Record<string, string>;

function addVariablesForColors({
  addBase,
  theme,
}: {
  addBase: (styles: Record<string, Record<string, string>>) => void;
  theme: (path: string) => Colors;
}) {
  const colors = theme("colors");
  const flattenColorPaletteTyped =
    flattenColorPalette as FlattenColorPaletteType;
  const allColors = flattenColorPaletteTyped(colors);
  const newVars: Record<string, string> = Object.fromEntries(
    Object.entries(allColors).map(([key, val]) => [`--${key}`, val]),
  );

  addBase({
    ":root": newVars,
  });
}

